"""Optional results dashboard.

Not required to use the framework — a plain markdown/JSON report is
generated by `python -m pef.cli evaluate` with no extra dependencies.
This viewer is a small convenience for browsing results interactively.

    pip install -r requirements-optional.txt
    streamlit run app.py
"""

from __future__ import annotations

import json
from pathlib import Path

import pandas as pd
import streamlit as st

RESULTS_PATH = Path(__file__).resolve().parent / "results" / "sample_results.json"

st.set_page_config(page_title="Prompt Evaluation Framework", layout="wide")
st.title("Prompt Evaluation Framework")

if not RESULTS_PATH.exists():
    st.warning(
        "No results file found. Run `python -m pef.cli evaluate` first to generate "
        f"{RESULTS_PATH}."
    )
    st.stop()

data = json.loads(RESULTS_PATH.read_text(encoding="utf-8"))
criteria = data["criteria"]

summary_rows = [
    {"prompt_id": r["prompt_id"], "composite": r["composite"], **r["mean_scores"]} for r in data["results"]
]
summary_df = pd.DataFrame(summary_rows).set_index("prompt_id")

st.subheader("Composite ranking")
st.dataframe(summary_df.style.format("{:.0%}"), use_container_width=True)

st.subheader("Score by criterion")
st.bar_chart(summary_df[criteria])

st.subheader("Sample outputs")
prompt_id = st.selectbox("Prompt version", [r["prompt_id"] for r in data["results"]])
selected = next(r for r in data["results"] if r["prompt_id"] == prompt_id)
for case in selected["cases"]:
    with st.expander(f"Case: {case['case_id']}"):
        st.write(case["primary_output"])
        st.json(case["scores"])
